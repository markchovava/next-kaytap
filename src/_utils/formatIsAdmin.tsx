import { isAdminData } from "@/_data/sample/isAdminData";


export const displayIsAdminName = (i: string) => {
    switch(i) {
        case isAdminData[0].value:
            return isAdminData[0].name;
        case isAdminData[1].value:
            return isAdminData[1].name;
        default:
            return "Not Added Yet"
    }
}